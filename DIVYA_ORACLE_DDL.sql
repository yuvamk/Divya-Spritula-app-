-- Users table (extends Supabase Auth)
CREATE TABLE DIVYA_USERS (
  ID              RAW(16) DEFAULT SYS_GUID() PRIMARY KEY,
  SUPABASE_UID    VARCHAR2(255) UNIQUE NOT NULL,
  ROLE            VARCHAR2(20) DEFAULT 'devotee'
                    CONSTRAINT chk_role CHECK (ROLE IN ('devotee','pandit','super_admin','ceo')),
  FULL_NAME       VARCHAR2(255) NOT NULL,
  PHONE           VARCHAR2(20) UNIQUE,
  AVATAR_URL      VARCHAR2(2000),
  PREFERRED_LANG  VARCHAR2(5) DEFAULT 'hi',
  GOTRA           VARCHAR2(100),
  NAKSHATRA       VARCHAR2(50),
  RASHI           VARCHAR2(50),
  OCCUPATION      VARCHAR2(100),
  TOTAL_DONATED   NUMBER(15,2) DEFAULT 0,
  CREATED_AT      TIMESTAMP DEFAULT SYSTIMESTAMP,
  UPDATED_AT      TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE TABLE DIVYA_TEMPLES (
  ID              RAW(16) DEFAULT SYS_GUID() PRIMARY KEY,
  PANDIT_ID       RAW(16) NOT NULL REFERENCES DIVYA_USERS(ID),
  NAME            VARCHAR2(500) NOT NULL,
  NAME_HI         VARCHAR2(500),
  DESCRIPTION     CLOB,
  DEITY           VARCHAR2(200),
  ADDRESS         VARCHAR2(1000),
  VILLAGE         VARCHAR2(200),
  DISTRICT        VARCHAR2(200),
  STATE           VARCHAR2(100),
  PINCODE         VARCHAR2(10),
  LATITUDE        NUMBER(10,7),
  LONGITUDE       NUMBER(10,7),
  LOCATION        SDO_GEOMETRY,
  COVER_PHOTO_URL VARCHAR2(2000),
  PHOTO_URLS      CLOB,                  -- JSON array
  IS_VERIFIED     NUMBER(1) DEFAULT 0,
  IS_ACTIVE       NUMBER(1) DEFAULT 1,
  QR_CODE_URL     VARCHAR2(2000),
  QR_TOKEN        VARCHAR2(100) UNIQUE DEFAULT RAWTOHEX(SYS_GUID()),
  DEVOTEE_COUNT   NUMBER(10) DEFAULT 0,
  RATING          NUMBER(3,2) DEFAULT 0,
  REVIEW_COUNT    NUMBER(10) DEFAULT 0,
  AI_SUMMARY      CLOB,                  -- Gemini-generated temple summary
  CREATED_AT      TIMESTAMP DEFAULT SYSTIMESTAMP,
  UPDATED_AT      TIMESTAMP DEFAULT SYSTIMESTAMP
);

-- Oracle Spatial index for nearby temple search
INSERT INTO USER_SDO_GEOM_METADATA VALUES (
  'DIVYA_TEMPLES','LOCATION',
  SDO_DIM_ARRAY(
    SDO_DIM_ELEMENT('LON',-180,180,0.005),
    SDO_DIM_ELEMENT('LAT',-90,90,0.005)
  ), 4326
);
CREATE INDEX idx_temples_spatial ON DIVYA_TEMPLES(LOCATION) INDEXTYPE IS MDSYS.SPATIAL_INDEX_V2;

