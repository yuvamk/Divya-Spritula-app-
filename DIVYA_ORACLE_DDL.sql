-- TRIGGERS
CREATE OR REPLACE TRIGGER trg_devotee_count
AFTER INSERT OR UPDATE OR DELETE ON DIVYA_TEMPLE_MEMBERS FOR EACH ROW
BEGIN
  UPDATE DIVYA_TEMPLES SET
    DEVOTEE_COUNT = (SELECT COUNT(*) FROM DIVYA_TEMPLE_MEMBERS
                     WHERE TEMPLE_ID = COALESCE(:NEW.TEMPLE_ID,:OLD.TEMPLE_ID) AND IS_ACTIVE=1)
  WHERE ID = COALESCE(:NEW.TEMPLE_ID,:OLD.TEMPLE_ID);
END;
